    private void btnDelItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelItemActionPerformed
        // TODO add your handling code here:
        int selected_row = tblGrocery.getSelectedRow();
        if (selected_row < 0) {
            JOptionPane.showMessageDialog(this, "Please select an item to remove.",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        GroceryItem item = (GroceryItem) tblGrocery.getValueAt(selected_row, 0);
        int dialog_result = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete " + item.getItemName()
                + " ?","Warning", JOptionPane.YES_NO_OPTION);
        
        if (dialog_result == JOptionPane.YES_OPTION) {
            enterprise.getGroceryList().RemoveGroceryItem(item);
            populateTable();
            JOptionPane.showMessageDialog(this, "Item deleted successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDelItemActionPerformed

    private void btnGetSuppliesInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetSuppliesInfoActionPerformed
        // TODO add your handling code here:
        if (! enterprise.fetchGroceryStockList()) {
            JOptionPane.showMessageDialog(this, "Cannot fetch data file from server ", "Warning",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (! enterprise.loadFreshGroceryStock()) {
            JOptionPane.showMessageDialog(this, "Failed to load complete Data from xontainers ", "Warning",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        populateTable();
        JOptionPane.showMessageDialog(this, "Latest Grocery Supplies list is fetched successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnGetSuppliesInfoActionPerformed


