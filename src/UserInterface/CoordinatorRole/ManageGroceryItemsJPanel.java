    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        GroceryItem item = enterprise.getGroceryList().AddItemtoGrocery();
        item.setItemName(txtItemName.getText());
        item.setItemNote(txtItemDesc.getText());
        item.setQuantity(Integer.parseInt(txtQuantity.getText()));
        populateTable();
        JOptionPane.showMessageDialog(this,"Item added successfully","Information", JOptionPane.INFORMATION_MESSAGE);
        txtItemDesc.setText("");
        txtItemName.setText("");
        txtQuantity.setText("");
    }//GEN-LAST:event_btnAddItemActionPerformed

