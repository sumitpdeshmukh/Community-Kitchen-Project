    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String userName = txtUsername.getText();
        String password = txtPassword.getText();
        Organization organization = (Organization) comboOrg.getSelectedItem();

        if (!organization.getUserAccountList().checkIfUsernameIsUnique(userName)) {
            JOptionPane.showMessageDialog(this, "Username: " + userName + " is not available. Choose Different name",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Person person = (Person) comboParticipant.getSelectedItem();
        Role role = (Role) comboRole.getSelectedItem();
        organization.getUserAccountList().createUserAccount(userName, password, person, role);

        populateData(organization);
        JOptionPane.showMessageDialog(this, "User Account added successfully for Participant " + person.getName(),
                "Information", JOptionPane.INFORMATION_MESSAGE);
        resetFields();
    }//GEN-LAST:event_btnCreateActionPerformed

