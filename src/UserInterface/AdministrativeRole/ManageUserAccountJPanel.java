    public void popOrganizationComboBox() {
        comboOrg.removeAllItems();        
        for (Organization organization : enterprise.getOrganizationList().getOrganizationList()) {
            if (!(organization instanceof VolunteerOrganization)) {
                comboOrg.addItem(organization);
            }
        }
    }

    public void populateParticipantComboBox(Organization organization) {
        comboParticipant.removeAllItems();
        for (Person person : organization.getParticipantList().getPartcipantList()) {
            comboParticipant.addItem(person);
        }
    }

    private void populateRoleComboBox(Organization organization) {
        comboRole.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            comboRole.addItem(role);
        }
    }

    public void populateData(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);

        if (!(organization instanceof VolunteerOrganization)) {
            for (UserAccount ua : organization.getUserAccountList().getUserAccountList()) {
                Object row[] = new Object[3];
                row[0] = ua.getParticipant().getName();
                row[1] = ua;
                row[2] = ua.getRole();
                model.addRow(row);
            }
        }

    }

